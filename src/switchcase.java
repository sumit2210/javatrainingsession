import java.util.Scanner;

public class switchcase {
    public static void main(String[] args){

        int x = 3;

        switch (x){

            case 1 :
                System.out.println("x equals 1");
                break;
            case 2 :
                System.out.println("x equals 2");
                break;
            case 3 :
                System.out.println("x equals 3");
                break;



        }

    }



}
